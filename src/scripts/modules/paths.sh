#!/usr/bin/env bash

### stores recurring paths for use in other scripts ###

# path to resources directory
res="../../../resources"

# path to module csv files in resources directory
res_mods="${res}/modules"

# path to HOME in resources directory
res_HOME="${res}/HOME"

# path to .config in resources directory
res_config="${res_HOME}/.config"

# path to .local/share in resources directory
res_local_share="${res_HOME}/.local/share"

# path to extension in resources directory
res_extensions="${res_local_share}/gnome-shell/extensions"

